package com.tinpan.model;

public class Profile {
	private String pictureLoc = "";
	private String bioBlurb;
	private String location;
	
	public String getPictureLoc() {
		return pictureLoc;
	}
	public void setPictureLoc(String pictureLoc) {
		this.pictureLoc = pictureLoc;
	}
	public String getBioBlurb() {
		return bioBlurb;
	}
	public void setBioBlurb(String bioBlurb) {
		this.bioBlurb = bioBlurb;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
}
